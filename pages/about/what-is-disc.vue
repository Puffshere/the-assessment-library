                                <li>focuses on  spontaneity, collaboration, and expressing enthusiasm</li>
                                <li>is described as convincing, charming, enthusiastic, charismatic, trusting, and optimistic</li>
                                <li>may be limited by lack of follow-through, impulsiveness and being disorganized</li>
                                <li>may fear loss of social approval, being alone or ignored; values spontaneity, dreaming, freedom of expression, and a variety of relationships</li>  
                                <li>values social acceptance and interaction, fun and excitement</li>
                            </ul>

                            <p>
