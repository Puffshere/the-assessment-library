                <h3 class="title">Who Created DISC?</h3>
                    <p>
                        Dr. William Moulton Marston was a lawyer and a physiological psychologist who first shared the DISC model in his 1928 book Emotions of Normal People.  
                        A man of many talents, Marston was also credited with helping to contribute to the first polygraph test, creating the character Wonder Woman, 
                        and authoring several self-help books.
                    </p>
                           
                    <p>
                        Marston conducted research on human emotions, and based on those findings, postulated that people illustrate their emotions using the behavior 
                        types of Dominance (D), Inducement (I), Submission (S), and Compliance (C), or DISC for short.
                    </p>    
                    
                    <p>
                        It was his belief that these behavioral types came from people's sense of self and their interaction with the environment.  In addition, 
                        he included two dimensions that influence people’s emotional behavior:
                    </p>  
                    
                    <p>
                        <ol>
                            <li>The first dimension was whether people view their environment as favorable or unfavorable.</li>
                            <li>The second dimension was whether a person perceives himself as having control or lack of control over his environment.</li>
                        </ol>
                    </p>
                    
                    <p>
                        Marston did not create an actual assessment tool from his theories, but based on his work, others did.  In 1956, Walter Clarke, an industrial 
                        psychologist first constructed an assessment that confirmed Marston’s theories.  He created the Activity Vector Analysis, a psychometric tool 
                        that was designed for use by businesses to help them choose qualified employees.   
                    </p>

                    <p>
                        In 1965, DISC findings were published in the Journal of Clinical Psychology.  The test format continued to evolve when a "Self-Description" 
                        test forced respondents to make a choice between two or more terms.
                    </p>
                    
                    <p>
                        While the assessments used today have evolved from the original work of Marston and Clarke, the same premise of four behavioral styles is 
                        used as the foundation and the continued development of the tools to embrace new science all the time comes from a rich history.
                    </p>
